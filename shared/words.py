def get_all_words():
    all_words = []
    for theme_name, theme_words in WORD_THEMES.items():
        if theme_name != "general":
            all_words.extend(theme_words)
    return all_words

def get_theme_words(theme):
    if theme == "general":
        return get_all_words()
    return WORD_THEMES.get(theme, [])

def get_words_from_themes(themes):
    """Получить слова из списка тем"""
    if not themes:
        return get_all_words()

    # Если в списке есть "general", возвращаем все слова
    if "general" in themes:
        return get_all_words()

    # Собираем слова из выбранных тем
    words = []
    for theme in themes:
        if theme in WORD_THEMES and theme != "general":
            words.extend(WORD_THEMES[theme])

    return words if words else get_all_words()

def get_random_word_pair(themes=None):
    """Получить сбалансированную пару слов из указанных тем

    Алгоритм:
    1. Первое слово выбирается случайно
    2. Второе слово подбирается так, чтобы сумма весов была сбалансированной:
       - Если первое слово легкое (1-2), подбирается тяжелое (4-5)
       - Если первое слово среднее (3), подбирается среднее (3)
       - Если первое слово тяжелое (4-5), подбирается легкое (1-2)
    """
    import random
    from shared.word_weights import get_word_weight

    if themes:
        words = get_words_from_themes(themes)
    else:
        words = get_all_words()

    if len(words) < 2:
        words = get_all_words()

    # Выбираем первое слово случайно
    first_word = random.choice(words)
    first_weight = get_word_weight(first_word)

    # Определяем целевой диапазон весов для второго слова
    if first_weight <= 2:
        # Легкое слово -> подбираем тяжелое
        target_min, target_max = 4, 5
    elif first_weight == 3:
        # Среднее слово -> подбираем среднее
        target_min, target_max = 3, 3
    else:
        # Тяжелое слово -> подбираем легкое
        target_min, target_max = 1, 2

    # Фильтруем слова по целевому диапазону весов
    suitable_words = [
        word for word in words
        if word != first_word and target_min <= get_word_weight(word) <= target_max
    ]

    # Если не нашли подходящих слов, берем любое другое
    if not suitable_words:
        suitable_words = [word for word in words if word != first_word]

    second_word = random.choice(suitable_words)

    return [first_word, second_word]

def get_available_themes():
    """Получить список доступных тем (без 'general')"""
    return [theme for theme in WORD_THEMES.keys() if theme != "general"]
